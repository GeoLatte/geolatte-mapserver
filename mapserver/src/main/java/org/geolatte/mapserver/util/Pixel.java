package org.geolatte.mapserver.util;

public class Pixel {

    final public int x, y;

    public Pixel(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public static Pixel valueOf(int x, int y) {
        return new Pixel(x, y);
    }

    public String toString() {
        return String.format("Pixel (%d, %d)", x, y);
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Pixel pixel = (Pixel) o;

        if (x != pixel.x) return false;
        if (y != pixel.y) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = x;
        result = 31 * result + y;
        return result;
    }
}
