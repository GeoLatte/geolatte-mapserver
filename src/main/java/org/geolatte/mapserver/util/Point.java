package org.geolatte.mapserver.util;

public class Point {

    final private static double TOLERANCE = 1E-8;
    final public double x, y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public static Point valueOf(double x, double y) {
        return new Point(x, y);
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Point point = (Point) o;

        if (Math.abs(point.x - x) > TOLERANCE) return false;
        if (Math.abs(point.y - y) > TOLERANCE) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result;
        long temp;
        temp = x != +0.0d ? Double.doubleToLongBits(x) : 0L;
        result = (int) (temp ^ (temp >>> 32));
        temp = y != +0.0d ? Double.doubleToLongBits(y) : 0L;
        result = 31 * result + (int) (temp ^ (temp >>> 32));
        return result;
    }

    @Override
    public String toString() {
        return String.format("(%f,%f)", x, y);
    }
}
